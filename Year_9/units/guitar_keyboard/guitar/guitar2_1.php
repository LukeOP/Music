        <h2>What Do Symbols Mean on Guitar TAB</h2>
        <p>Now that you know what the lines and numbers represent on Guitar TAB, let’s look at the important symbols used.</p>
        <br>
        <p>Symbols on Guitar TAB represent different guitar techniques such as slides, bends, hammer-ons, palm-muting, and more. When you see a symbol in Guitar TAB, it is telling you to perform a specific type of technique.</p>
        <br>
        <p>There are two sets of symbols to learn for Guitar TAB. Text-based Guitar TAB uses one set of symbols and formal Guitar TAB uses a different set of symbols.</p>
        <br>
        <p>Let’s go through all of the main symbols you will likely see in Guitar TAB.</p>

        <h3 class="border">What does h mean in Guitar TAB</h3>
        <p><strong>‘h‘</strong> in Guitar TAB is short for ‘hammer-on’. This is when you play a note and hammer-on to a higher note.</p>
        <p>In text-based Guitar TAB this is shown as ‘h’ in between two notes. In formal Guitar TAB, this is shown as a curved line over the two notes as shown below and an H above the staff:</p>
        <div class="image"><img src="/Year_9/images/guitar_and_keyboard/hammer-on-guitar-tab-h-symbol.jpg" alt="" class="img-style"></div>
        <p>Both of the above Guitar TABs are showing the exact same thing to play.</p>
        <br>
        <p>2h4 means play the 2nd fret, then hammer-on to the 4th fret. 2h4h5 means to do two hammer-ons in a row (you only pick the first note).</p>

        <h3 class="border">What does p mean in Guitar TAB</h3>
        <p><strong>'p'</strong> in Guitar TAB is short for ‘pull-off’. This is when you play a note and pull-off to a lower note. It’s basically the opposite of a hammer-on.</p>
        <div class="image"><img src="/Year_9/images/guitar_and_keyboard/p-symbol-guitar-tab.jpg" alt="" class="img-style"></div>
        <p>The same curved line is used in formal Guitar TAB as is used for hammer-ons, so you simply need to look at whether the number is higher or lower to know which technique to use.</p>
        <br>
        <p>Hammer-ons and pull-offs can be combined (known as <i>legato</i> meaning "smoothly") all under the same curved line.</p>
