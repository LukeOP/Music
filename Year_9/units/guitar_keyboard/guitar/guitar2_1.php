        <h3 class="border">What does / or \ mean in guitar TAB</h3>
        <p>A slash ( <strong>/ </strong>or <strong>\ </strong>) in Guitar TAB is the symbol for a slide.</p>
        <br>
        <p>The type of slash used tells you whether you need to slide up to a note ‘/’ or slide down to a note ‘\’.</p>
        <br>
        <p>To know which slash symbol is which, think of the slash symbol as a slide you walk up to from the left. If the slash slopes downwards like \, you slide down on guitar. If the slash slopes upwards like /, you slide up on guitar.</p>
        <div class="image"><img src="/Year_9/images/guitar_and_keyboard/slash-symbol-guitar-tab.jpg" alt="" class="img-style"></div>
        <p>The same symbols are used in text-based and formal Guitar TAB so it’s easy to identify slides. Some old text-based Guitar TAB may show s instead of a slash (eg: 7s5).</p>
        <br>
        <p>In formal Guitar TAB, you may also notice a curved line over the top of the slide, this is a reminder that you only pick the first note of the slide. If there isn’t a curved line, it means to pick both notes.</p>



