<div class="page-content">
    <h1 class="page-heading">Lesson 1 - The Parts of the Guitar</h1><br>
    <div class="text-content">
        <h2 class="border">The Head or Headstock</h2>
        <p>With the exception of ‘headless’ guitars, all guitars have a headstock. This is where the strings connect and where you tune the strings up. 
            The below examples show how different headstocks can be across different guitars:</p>
        <div class="image"><img class="img-style" src="/Year_9/images/guitar_and_keyboard/guitar-headstocks.jpg"></div>
        <p>The two most common headstock layouts are 3+3 style where there are three tuners per side and 6 in line which has all six tuners on one side. 
            There are other layouts as seen above.</p><br>
        <p>Some headstocks are angled to varying degrees while others are straight. The angle of the headstock affects tuning stability.</p>

        <h2 class="border">Tuning Pegs or Tuning Machines</h2>
        <p>Tuning machines or pegs are found on the headstock. These pegs allow you to thread the string through and tune up the guitar. 
            Some tuning machines are ‘locking’ tuners, while others rely on tying the string in a certain way to hold tune.</p>
        <div class="image"><img class="img-style" src="/Year_9/images/guitar_and_keyboard/guitar-tuning-pegs.jpg" style="margin-left: 17%;"></div>
        <p>Good quality tuning machines can greatly improve the stability of the guitar’s tuning.</p>

        <h2 class="border">The Nut</h2>
        <p>The nut is where the string rests on the guitar. Think of it as the ‘zero’ fret. The nut is one of the most important parts of the guitar for overall playability and sound quality. 
            A poorly fitted or worn out nut can cause string buzzing, tuning stability problems, or action issues.</p>
        <div class="image"><img class="img-style" src="/Year_9/images/guitar_and_keyboard/guitar-nut.jpg" style="margin-left: 20%;"></div>
        <p>The nut can be made out of different materials such as ebony, plastic, bone, graphite, or ivory. As you can see from the example to the right, they can also be made out of metal. 
            The nut on the right is a ‘locking’ nut which clamps down onto the strings to hold them in tune. </p>

        <h2 class="border">The Neck</h2>
        <p>As covered earlier, the neck is the part of the guitar that connects the headstock to the body of the guitar. People often mix up the terms neck and fretboard. 
            The fretboard (covered later) is a part of the neck.</p><br>
        <p>The guitars below show the different designs necks can have. Some are made from one solid piece of wood, while others are made by laminating multiple pieces of wood together.</p>
        <div class="image"><img class="img-style" src="/Year_9/images/guitar_and_keyboard/guitar-necks.jpg" style="margin-left: 20%;"></div>
        <p>Different woods can be used to build a neck such as: maple, mahogany, wenge, purple heart, ebony, rosewood, walnut, and more. 
            Different types of wood offer different levels of strength for the neck.</p><br>
        <p>Necks can also come in different finishes. In the above examples, you can see how some necks have a gloss while others have a more satin or matte finish. 
            Many guitars have painted necks with different finishes. Some guitarists like the feel of a smooth satin neck, while others prefer something with more grip.</p>

        <h2 class="border">The fretboard or fingerboard</h2>
        <p>The fretboard is the front part of the guitar’s neck. The three main types of wood used for fretboards are ebony, maple, and rosewood as shown below:</p>
        <div class="image"><img class="img-style" src="/Year_9/images/guitar_and_keyboard/guitar-fretboards.jpg" style="margin-left: 20%;"></div>

        <h2 class="border">The Frets</h2>
        <p>Frets are the metal wire running across the fretboard. When you push your fingers down against the strings, the strings press against the frets.</p>
        <div class="image"><img class="img-style" src="/Year_9/images/guitar_and_keyboard/guitar-frets.jpg"></div>

        <h2 class="border"2>The Bridge</h2>
        <p>The bridge is the other point where the strings are connected (the first point is the nut). 
            There are many different types of bridges and each type offers different benefits. 
            Most acoustic bridges are more or less the same, while electric guitar bridges can vary greatly.</p>
        <div class="image"><img class="img-style" src="/Year_9/images/guitar_and_keyboard/guitar-bridges.jpg"></div>
        <p>The way you connect the strings to the guitar depends on the type of bridge. The acoustic bridge on the far left uses pins to hold the ball ends of the strings down. 
            The other acoustic bridge on the far right requires you to tie the strings to the bridge.</p><br>

        <h2 class="border">The Sound Hole</h2>
        <p>Acoustic guitars have a large hole in the body between the bridge and the fretboard. The purpose of this hole is to give us the loud sound we expect from an acoustic guitar. 
            It’s also a cause for frustration when you drop your pick into the hole (it will happen).</p><br>
        <p>Almost all acoustic guitars have sound holes like the left and middle examples below.
            The far right example shows a different type of sound hole on an acoustic guitar. 
            Acoustic guitars like this one, and other acoustic guitars without sound holes are designed for live performance. </p>
        <div class="image"><img class="img-style" src="/Year_9/images/guitar_and_keyboard/guitar-sound-holes.jpg"></div>

        <h2 class="border">the Pick Guard</h2>
        <p>Some acoustic and electric guitars use a plastic sheet called a pickguard to protect the guitar’s body from your pick or fingers.</p>
        <div class="image"><img class="img-style"  src="/Year_9/images/guitar_and_keyboard/guitar-pickguards.jpg"></div>
        <p>Whether you need a guitar with a pickguard or not depends on your style of playing. 
            If you strum the strings hard in a big arc, your pick is likely to hit the body of the guitar. Over time this can cause a lot of wear on the guitar.</p><br>

        <h2 class="border">The Strings</h2>
        <p>Strings are a crucial part of a guitar as they are responsible for your tone and playability. 
            If you’ve ever replaced a guitar’s old strings with a new set, you’ll know how much they can affect your tone.</p>
        <div class="image"><img class="img-style" src="/Year_9/images/guitar_and_keyboard/guitar-strings.jpg"></div>
        <p>Classical guitars (like the ones we use at school) use nylon strings (only the first three strings are nylon) and all other guitars use steel strings. 
            There are many different types with different coatings and features that are beyond the scope of this lesson.</p><br>
        
