        <p>So we have learnt some chords and how to read TAB, but we need to put them into context by playing a song.</p>
        <h3 class="border">Chord Charts</h3>
        <p>Chord charts are a way to display what chords should be played and at which point in a song.
            They often look like lyrics with Letters above them like this example below:</p>
        <div class="image"><img src="/Year_9/images/guitar_and_keyboard/guitar/O-come-to-the-alter-chords.jpg" alt="" class="img-style"></div>
        <p>Most chord charts have some basic information:</p>
        <ul>
            <li><strong>Title</strong> - O Come To The Altar</li>
            <li>The <strong>Artist</strong> - Elevation Worship</li>
            <li>A <strong>key</strong> to play the song in - A Major</li>
            <li>A <strong>tempo</strong> to indicate how fast if should be played - 140 beats per minute</li>
            <li><strong>Lyrics</strong> broken down into sections based on the song strucutre</li>
            <li>Letter indications for <strong>chords</strong> to be played during the song</li>
        </ul>

