    <h1 class="page-heading">Lesson 1 - The Parts of the Guitar</h1><br>
    <div class="text-content">
        <p>Learning the parts of the guitar for both acoustic and electric guitars is important for every guitarist.
            If you go to a store looking at buying a guitar, you’re going to hear a lot of part names. If you don’t know what some of those part names are, it will be hard for you to pick the right guitar for your needs.
        </p><br>
        <p>In this lesson you will learn all the important guitar part names, what they look like, what they do, and different variations for each part.
            By the end of this lesson, you will be able to talk to any guitarist and fully understand any guitar parts they mention.
        </p>
        <h2>Main Parts of the Guitar</h2>
        <p>The diagram below shows the main parts for a range of both acoustic and electric guitars</p><br>
        <div class="image"><img class="img-style" src="/Year_9/images/guitar_and_keyboard/parts_of_the_guitar.jpg"></div>
        <br><br>
        <p>While all of the above guitars have different parts, design, and features, the main parts are the same.
            All of the above guitars have three main parts:</p><br>
        <ul>
            <li>The <strong>head </strong>of the guitar shown in <span style="color: green;">green</span></li>
            <li>The <strong>neck </strong>of the guitar shown in <span style="color: red;">red</span></li>
            <li>The <strong>body </strong>of the guitar shown in <span style="color: blue;">blue</span></li>
        </ul><br>
        <p>As you can see, there’s a lot of differences between the above guitars. While all have a head, neck, and body, the shape, features, and design are all different across the guitars.
            We will dig deeper into each part later on. The main point to remember is that all guitars start out with these main parts.</p>
        <h2>Acoustic Guitar Parts Diagram</h2>
        <p>The diagram below shows two different acoustic guitars with the parts labeled. The guitar on the left is a <strong>steel string acoustic</strong> and the guitar of the right is a <strong>nylon string acoustic.</strong>
            Most of the guitars we have here at Middleton Grange School are nylon string acoustics</p><br>
        <div class="image"><img class="img-style" src="/Year_9/images/guitar_and_keyboard/acoustic-guitar-parts-diagram.jpg"></div><br><br>
        <p>While the two types of acoustic guitars are very different in how they sound and play, many of the parts are similar. In the above diagram, the shape of the body is different. The steel-string acoustic has a <strong>cutaway</strong> to give better access to the upper frets.
            The steel-string acoustic has dot inlays on the <strong>fingerboard</strong> (also called <strong>fretboard</strong>) to mark out important fret positions. While the nylon string acoustic doesn’t have dot inlays, it does have markers on the side of the neck which is only visible from the guitarist’s view.
            Most guitars have a combination of markers on the side of the neck and on the fretboard.</p><br>

