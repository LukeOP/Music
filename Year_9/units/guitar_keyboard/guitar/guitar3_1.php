        <h2>Chord Diagram Tips and Tricks</h2>
        <h3 class="border">Tip 1. The Numbers are only a suggestion</h3>
        <p>There will be times when you see a chord diagram using different numbers on a chord you already know. 
            Take a look at the below chord diagrams as an example:</p>
        <div class="image"><img src="/Year_9/images/guitar_and_keyboard/guitar-chord-diagrams-with-finger-numbering.jpg" alt="" class="img-style"></div>
        <p>The diagrams are all for the exact same chord, but the numbers a different.</p>
        <br><p>This is because some chords can be played with different finger combinations. 
            There is no ‘right’ way to play the above chord. 
            You can use any finger combination and it will still sound the same.</p>
        <br><p>Here are the three different ways you can play Em as shown in the above chord diagrams:</p>
        <div class="image"><img src="/Year_9/images/guitar_and_keyboard/em-open-chord.jpg" alt="" class="img-style"></div>
        <p>If you try to play all three, you might notice that some finger combinations feel more comfortable than others. 
            As you develop your guitar skills, you will learn that sometimes it makes sense to change which fingers you use to play a chord. 
            Different finger combinations make it easier to switch into different chords.</p>
        <br><p>The key point to remember is that the numbers in the diagrams are only suggestions. 
            If you think of a different combination of fingers to play the chord, that’s fine.</p>
        
        <h3 class="border">Tip 2. Using Guitar Chord Diagrams With a Weird 'X'</h3>
        <p>Sometimes you might see a chord diagram like this one:</p>
        <div class="image"><img src="/Year_9/images/guitar_and_keyboard/chord-diagram-weird-x.jpg" alt="" class="img-style"></div>
        <p>This chord diagram is telling us to play the low E string, skip the A string, then strum all the other strings. How is this possible?</p>
        <br><p>The way to do this is to use your finger to rest against the A string and mute it.</p>
        <br><p>In the below photo, you can see that I use the side of my second finger to rest against the A string. 
            Then I can strum all six strings and the fifth string will stay muted.</p>
        <div class="image"><img src="/Year_9/images/guitar_and_keyboard/guitar-chord-diagram-x-hand.jpg" alt="" class="img-style"></div>
        <div class="task"><h3><img src="/images/task-guitar.svg" alt="" class="icon">Your Turn</h3>
        <p>Have a go at playing the chord above and muting the A string (second thickest string). Can you still make the chord sound complete?</p>
        </div>

        <h3 class="border">Tip3. Changing Keys With a Capo</h3>
        <p>If you are wanting to play a song but it is in a key with difficult to play chords you can use a capo to change the key.</p>
        <br><p>A capo essentially replaces the roll of the nut and you can move it further up the fretboard.</p>
        <br><p>For example: If you need to play a G chord you could play it normally or you can put a capo on the 3rd fret and then play an E chord.</p>
        <br><p>This works because G is 3 semi-tones higher than E</p>
        <div class="image"><img src="/Year_9/images/guitar_and_keyboard/chord-diagram-with-capo.jpg" alt="" class="img-style"></div>
        <p>If this is confusing at the moment, don't worry. As you continue to learn some basic music theory and the notes on the fretboard, this will become clearer.</p>
        
