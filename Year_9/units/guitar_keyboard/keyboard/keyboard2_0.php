    <h1 class="page-heading">Musical Notation</h1>
    <p>In the last lesson we introduced the notes and how they are placed on the keyboard.
        This lesson will put notes on a page by introducing musical notation, the written communication of music. </p>
    <h2 class="header">What is Musical Notation?</h2>
    <div class="text-content">
        <p>Music is a language. Like any language, music has a written form. It’s about communication.
            Notation gives musicians around the world a medium to communicate.
            A composer notes down a piece of music with specific symbols, and if you can read music, you can understand it.
            You most likely will never meet the composer, separated by continents and centuries, but communication still takes place.</p>
        <div class="image"><img src="/Year_9/images/guitar_and_keyboard/keyboard/staff-notation-l.webp" alt="" class="img-style">
            <p>Piece for piano in staff/stave notation</p>
        </div>
        <h2>Notation Basics</h2>
        <h3 class="border">The Staff/Stave</h3>
        <p><a class="tooltip">Stave<span class="tooltiptext">also known as the 'staff' in US English</span></a> notation is structured around the <strong>grand stave</strong> two staves of 5 lines and 4 spaces, connected by a <a href="" class="tooltip"> brace <span class="tooltiptext">Curly bracket to the left of the stave</span></a> on the left.
            The top stave is usually marked with a <strong>treble clef</strong> and typically played with the right hand while the bottom stave is usually marked with a <strong>bass clef</strong>
            and typically played with the left hand. Middle C lies in the gap between the staves, on an <strong>imaginary line.</strong>
            Just as it is the centre point for orientation on the keyboard, so it is on the stave.
        </p>
        <div class="image"><img src="/Year_9/images/guitar_and_keyboard/keyboard/middle-c-grand-piano-staff-l.png" alt="" class="img-style">
            <p>Middle C on the grand piano stave</p>
        </div>
        <p>The lines and spaces of the staves are home to various musical symbols, including notes. Notes can sit on a line or in a space. The height of the note determines the pitch. A higher line means a higher pitch, so moving up the stave represents moving right along the keyboard.
            We add ledger lines above or below the stave if a note is higher or lower than the 5 stave lines.</p>

        <h3 class="border">The Notes</h3>
        <p>We’ll focus on the top stave for now, the <strong>treble clef</strong>.
            Find middle C on the keyboard and on the stave below.
            From here, follow the sequence of the musical alphabet (A to G) to name all the notes found on the treble stave.
            On the stave, the head of the note shows the pitch.</p>
        <div class="image"><img src="/Year_9/images/guitar_and_keyboard/keyboard/notes-treble-staff-l.png" alt="" class="img-style"></div>
        <p>The first five notes we played with our right hand during <i>'Ode To Joy'</i> are found in the bottom half of the treble stave.
            Our C is on the middle C ledger line, D is just below the first line of the stave, E is on the first line, F is in the first space, and G rests on the second line.</p>

        <h3 class="border">Rhythm</h3>
        <p>Moving from left to right represents moving forward through the music. Just as the position of the note tells you which key to play, the shape of the note tells you how long to play it.</p>
        <br>
        <p>A <span class="tooltip"><strong>Semi-breve</strong><span class="tooltiptext">also known as a 'whole note' in US English</span></span> whole note is an empty circle and lasts four counts.</p>
        <p>A <span class="tooltip"><strong>Minim</strong><span class="tooltiptext">also known as a 'half note' in US English</span></span> adds a stem and lasts two counts.</p>
        <p>A <span class="tooltip"><strong>Crotchet</strong><span class="tooltiptext">also known as a 'quater note' in US English</span></span> fills in the circle and lasts one count.</p>
