    <h1 class="page-heading">Learning Chords</h1>
    <p></p>
    <div class="text-content">
        <p>Chords define the harmony of a song, and are made by playing more than one note at the same time.
            They can be simple or complicated, but even beginner piano chords can create fun and exciting music.
            In fact, there are very few songs that don’t use chords in some way, especially in rock music!</p>

        <h2 class="border">What Is A Chord?</h2>
        <p>A chord is created when more than one note is played at once, and contains two, three, or more individual notes.
            On the piano, this means you push down more than one key at the same time.</p><br>
        <p>All piano chords contain a root note (this is the note the chord is named after) as well as one or more additional notes.
            Basic piano chords often consist of only two or three notes, while the more advanced chords tend to incorporate even more notes.</p><br>
        <p>The most common type of keyboard or piano chord is a triad, or three-note chord.
            A triad contains a root note and two other notes, most often the notes that produce the intervals of a third and fifth above the root note.</p>
        <p>One way to get the basic shape of a triad is to place your thumb and fingers on adjacent white keys and push down with your thumb, middle finger and pinky.
            Learning this technique will set you up to play various basic piano chords with ease.</p>

        <h2 class="border">What Are Piano Intervals?</h2>
        <p>The distance between piano notes, called an interval, determines how they sound when played together.
            Intervals are measured in <strong>semi-tones</strong> (half-steps) and <strong>tones</strong> (whole-steps).</p>
        <ul>
            <li><strong>Semi-tones: </strong>A semi-tone is the distance from one key to the next key immediately to its right or left.</li>
            <li><strong>Tones: </strong>A tone is the distance of two semi-tones.</li>
        </ul>
