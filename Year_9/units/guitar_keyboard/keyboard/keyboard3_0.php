        <h2 class="border">How To Play Major Chords</h2>
        <p>To play a major chord, begin by choosing a root note, which can be any of the keyboard notes From the root note, count up two tones.
            This note is the “third,” named for being the third note in the key beginning with the root note. From the third, count up one-and-a-half tones, or three semi-tones. This note is the “fifth.”</p>
        <br>
        <p>When you play these three keyboard notes together, you hear a major triad, which has a happy sound.
            Major keyboard chords are used in almost every rock and pop song.</p>

