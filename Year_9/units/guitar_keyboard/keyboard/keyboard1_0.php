    <h2 class="header">Starting position and orientation</h2>
    <div class="text-content">
        <p>Look down at the keys. Each of them represents a different note, moving from low to high as you move from far left to far right of the keyboard.
            To help you find your starting point, look at the black keys.
            You will notice an alternating pattern: groups of two and three black keys.</p>
        <div class="vid"><video controls autoplay loop class="vid-style">
                <source src="/Year_9/images/guitar_and_keyboard/keyboard/twogroups.webm">
            </video>
            <p>Groups of two and three black keys</p>
        </div>
        <p>The keys are named after letters of the alphabet. Every white key immediately to the left of a group of two black keys is a C.
            Find the middle of the keyboard, and the C to the left of the group of two black keys.
            This is <strong>middle C</strong>, and will be your center point for orientation.</p>
        <div class="vid"><video controls autoplay loop class="vid-style">
                <source src="/Year_9/images/guitar_and_keyboard/keyboard/middlec.webm">
            </video>
            <p>Finding middle C</p>
        </div>
        <p>Once you have found middle C, place your right thumb on it. Remember the hand position above.
            The left side of the thumb, near the tip, should be touching middle C, while the rest of your fingers curl around the imaginary ball (or knee).</p>
        <br>
        <p>Now spread the rest of your fingers on the keys following middle C, assigning one finger to each of the next four white keys, which are D, E, F, and G. This is called the <strong>C position.</strong>
            It’ll be your starting position for playing your first melodies.
            Ignore the black keys for now, we will come to them later.</p>
        <div class="image"><img src="/Year_9/images/guitar_and_keyboard/keyboard/c-position-v2-l.webp" alt="" class="img-style"></div>

        <h3 class="border">The Other Notes:</h3>
        <p>You will have noticed that the notes follow the structure of the alphabet. The same goes for the two notes below C (A and B).
            Since there are only seven different white keys, we only use the first seven letters in the alphabet:
            A to G. In other words, the white key that comes after G is an A, then the next is a B, the one after is again a C, and so on.
            Learning piano, we initially start on C and not on A because many easy songs for beginners can be played in the C position with only white keys.</p>
        <br>
        <p>The <strong>eight</strong> notes from A to the next A make up one <strong>octave</strong> (from the Latin “octo”). If you experiment with playing the same note in different octaves, you will notice that they sound the same, only higher or lower.</p>
        <div class="image"><img src="/Year_9/images/guitar_and_keyboard/keyboard/octave-l.jpg" alt="" class="img-style"></div>
        <p>If you look up and down the keyboard you can see that each of the C notes is located to the left of the next group of two black keys.
            Similarly, each of the F notes is located to the left of a group of three black keys. Once you identify the pattern, or simply count up from the C notes, you can name every white key on the piano.
            The lowest note on most pianos is an A or C, but always use the middle C position to orient yourself as you will use that area the most.</p>
        <div class="task">
            <h3><img src="/images/task-piano.svg" alt="" class="icon">Your Turn</h3>
            <p>Practice playing all five keys of the C position in order, one at a time, from low to high. Play each key firmly so the note rings out, then release before you play the next.</p>
            <br>
            <p>Try playing the notes backwards, or skipping notes, see how it feels and sounds.</p>
            <br>
            <p>Experiment again with different dynamics, play these five notes harder or softer, hold them for longer or shorter periods.</p>
        </div>

        <h3 class="border">Playing Your First Melodies</h3>
        <p>We will cover the basics of written music later, but for now we will get you playing your first melodies using a basic form of notation.</p>
        <div class="task">
            <h3><img src="/images/task-piano.svg" alt="" class="icon">Your Turn</h3>
            <p>Start in C position, assign a finger to the notes from C to G, and play the notes written below, raising your finger from the key as you play the next.
                Hold each note for the same length of time, but when the gap is longer hold the note for longer. Don’t worry if it sounds rough, this is more than enough to get you started.
                We will introduce a far better way to keep time and represent timing shortly.</p>
            <br>
            <p>Try this simplified version of Beethoven’s <i>'Ode To Joy'.</i></p>
