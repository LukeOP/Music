    <h1 class="page-heading">Lesson 3 - Chord Diagrams</h1><br>
    <div class="text-content">
        <div class="image"><img src="../../images/guitar_and_keyboard/chord-diagrams-banner.jpg" alt="" class="img-style"></div>
        <h2>Chord Diagrams</h2>
        <p>Guitar chord diagrams or charts are important to understand as they give you a quick way of learning new chords. 
            Once you learn how to read guitar chord diagrams, you’ll be able to start learning countless chord-based songs.</p>
        <p>In this lesson you will learn:</p><br>
        <ul>
            <li>The five points on how to read guitar chord diagrams</li>
            <li>Tips to help you understand some strange chord diagrams</li>
        </ul><br>
        <p>By the end of this visual guide to chord diagrams, you’ll know everything you need to about chord diagrams.</p>
        <h2>The Five Main Points</h2>
        <ul>
            <li>The vertical lines represent the guitar strings</li>
            <li>The horizontal lines represent the frets with the top line representing the guitar nut</li>
            <li>The black dots represent where you place your fingers</li>
            <li>The numbers tell you which fingers to use</li>
            <li>An ‘O’ tells you to play a string and an ‘X’ tells you to not play a string</li>
        </ul>
        <div class="image"><img src="../../images/guitar_and_keyboard/how-to-read-guitar-chords.jpg" alt="" class="img-style"></div>
        <p>Let's go through the five points above to understand how we should play a chord using a diagram like this one.</p>

        <h3 class="border">1. Vertical Lines</h3>
        <p>The first point is the vertical lines. The vertical lines represent the strings on your guitar.</p><br>
        <p>The far-left line represents the thickest string on your guitar (low E) and the far-right line represents the thinnest string on your guitar (high E).</p>
        <br><p>Here is a diagram labeling the strings on a blank chord diagram:</p>
        <div class="image"><img src="../../images/guitar_and_keyboard/guitar-chord-diagram-strings.jpg" alt="" class="img-style"></div>
        <p>So when you read a guitar chord diagram, it’s showing you your guitar from a sideways point of view.</p>

        <h3 class="border">2. Horizontal Lines</h3>
        <p>The second point to know is the horizontal lines. 
            The horizontal lines represent the frets on your guitar with the thickest top line representing the guitar nut.</p>
        <div class="image"><img src="../../images/guitar_and_keyboard/guitar-chord-diagram-frets.jpg" alt="" class="img-style"></div>
        <p>In the above photo, you can see how the frets match up to the lines in the blank diagram. 
            You also know that the vertical lines represent the six strings, so the above photo shows you exactly how to think about a chord diagram.</p>

        <h3 class="border">3. Black Dots</h3>
        <p>The third point to know is the black dots. Sometimes these dots are numbered and sometimes they aren’t (explained later). 
            The black dots show you where to place your fingers on the guitar fretboard.</p>
        <div class="image"><img src="../../images/guitar_and_keyboard/guitar-chord-diagram-black-dots.jpg" alt="" class="img-style"></div>
        <p>The first dot is on the first fret on the second (B) string. The second dot is on the second fret on the fourth (D) string. 
            The third dot is on the third fret on the fifth (A) string.</p>
        <br><p>The above diagram is telling us we need to use three fingers to play this chord. 
            To know which fingers to use, we look at the numbers as shown in the next step.</p>

        <h3 class="border">4. Numbers</h3>
        <p>The fourth point to know is the numbers. 
            The numbers on the black dots or below the chord diagram tell us which fingers to use to play the notes.</p>
            <div class="image"><img src="../../images/guitar_and_keyboard/guitar-chord-diagram-numbers.jpg" alt="" class="img-style"></div>
        <p>As you can see from the above diagrams, sometimes the numbers are shown on the black dots, sometimes they’re shown below the diagram, and sometimes they’re not shown at all. 
            If a diagram doesn’t show you any numbers, you need to figure out which fingers to use on your own.</p>
        <br><p>Here is how we number the fingers on our fretting hand:</p>
        <div class="image"><img src="../../images/guitar_and_keyboard/left-hand-finger-numbering.jpg" alt="" class="img-style"></div>
        <p>Once you memorize how to number your fingers, you’ll be able to match up which fingers to use for any diagrams. 
            The thumb is numbered ‘T’, although you’re unlikely to see it used in many chords.</p>
        <div class="note">
            <h3><img src="/images/notes.svg" alt="" class="icon">Note for left-handed musicians:</h3>
            <p>It may seem easier to play guitar in reverse (with your right hand making the shapes); however, this is not something I would recommend.</p>
            <br><p>The reason for this is that chord diagrams are almost never written for left-handed players and it could become confusing to try and work out where your fingers should go.</p>
            <br><p>Another reason to learn guitar "Right-handed" is because the majority of guitars you come across in life will be strung this way.
                Being able to play the majority of guitars is worth putting in the little bit of extra effort now while you are learning.</p>
        </div>
        <p>You can see from the below photo how each finger matches the black dots on the diagram.</p>
        <div class="image"><img src="../../images/guitar_and_keyboard/guitar-chord-diagram-fingers.jpg" alt="" class="img-style"></div>
        <p>The first finger (index) is on the second fret on the second string. That matches the ‘1’ dot on the diagram. 
            The second finger (middle) is on the second fret on the fourth string, matching the ‘2’ dot. 
            The third finger (ring) is on the third fret on the fifth string, matching the ‘3’ dot. 
            The fourth finger isn't used because there isn’t a dot with a ‘4’ on it on the diagram.</p>

        <h3 class="border">5. 'O's and 'X's</h3>
        <p>Now we have the right fingers on the right frets. The fifth point to know is which strings to play. 
            The ‘O’ and ‘X’ you see above each vertical line tells us whether we should play a string or not.</p>
        <br><p>If a string doesn’t have an ‘O’ or an ‘X’ above the string, check if there is a dot on the string. 
            A dot on the string means you need to play the string.</p>
        <br><p>If there is an ‘O’ above a string, it means to play that string as part of the chord. 
            If there is an ‘X’ above a string, it means not to play that string.</p>
        <div class="image"><img src="../../images/guitar_and_keyboard/guitar-chord-diagram-strumming.jpg" alt="" class="img-style"></div>
        <p>In the above chord diagram, there are two ‘O’s and one ‘X’. This means we skip strumming the low E string and play all the other strings 
            (the other three strings without an ‘O’ have dots on them).</p>
        <br><p>In the below photo you can see that I have my pick ready to strum the five strings and skipping the low E string.</p>
        <div class="image"><img src="../../images/guitar_and_keyboard/guitar-chord-diagram-strumming-2.jpg" alt="" class="img-style"></div>
        <p>Some chord diagrams will have you strum all strings and some may only use five or four strings. 
            Always look at how many strings to strum or else your chords may sound wrong.</p>
        
