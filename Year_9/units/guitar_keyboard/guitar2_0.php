        <h2>What is TAB?</h2>
        <p>Guitar TAB (or tablature) is a way to write guitar music that represents the positioning and strings to play and in what order. It is incredibly easy to understand once you know what all the symbols and numbers mean.</p>
        <br><p>In this lesson we will go through how to read Guitar TAB that you might find on websites such as <a href="https://www.ultimate-guitar.com/" class="text-link">Ultimate Guitar.</a></p>
        
        <h2>Guitar TAB Layout</h2>
        <p>The first thing you need to understand with Guitar TAB is the basic layout.</p>

        <h3 class="border">The Lines</h3>
        <p>Guitar TAB has <strong>six horizontal lines</strong> to represent the six strings on the guitar.</p>
        <br><p>As you can see in the diagram below, the six lines match up to the six strings on your guitar:</p>
        <div class="image"><img src="../../images/guitar_and_keyboard/how-to-read-guitar-tab-lines.jpg" alt="" class="img-style"></div>
        <p>Looking at Guitar TAB is like looking down at your guitar in your hands. The bottom line in Guitar TAB matches the lowest pitch string (low E) on your guitar.</p>
        <br><p>Text-based and formal Guitar TAB sometimes displays the string tunings for each line as shown below:</p>
        <div class="image"><img src="../../images/guitar_and_keyboard/text-based-guitar-tab-lines.jpg" alt="" class="img-style"></div>
        <p>The key point to remember is that the <strong>top line</strong> on Guitar TAB matches the <strong>highest pitch string</strong> on your guitar and the lowest line matches the lowest pitch string on your guitar.</p>

        <h3 class="border">The Numbers</h3>
        <p>Each line on Guitar TAB matches a string on your guitar. Whenever you see a number on a line in Guitar TAB, it is an instruction to play a specific note on that string.</p>
        <br><p>The numbers on Guitar TAB represent the fret numbers on a string. 5 would mean the fifth fret on your guitar. 12 would mean the twelfth fret. 0 would mean the open string (zero fret).</p>
        <br><p>Take a look at the below diagram for examples of different Guitar TAB numbers and how each number matches a note on the guitar:</p>
        <div class="image"><img src="../../images/guitar_and_keyboard/how-to-read-guitar-tab-numbers.jpg" alt="" class="img-style"></div>
        
        <br><p>There are two ways numbers can be written in Guitar TAB and it affects the way you play them.</p>

        <h2>The Order of Playing Notes</h2>
        <h3 class="border">Numbers Stacked on Top of Each Other</h3>
        <p>When numbers on Guitar TAB are stacked on top of each other, it is telling you to play those numbers at the same time. In other words, strum a chord.</p>
        <div class="image"><img src="../../images/guitar_and_keyboard/guitar-tab-chords.jpg" alt="" class="img-style"></div>
        <p>All of the above numbers in the Guitar TAB are stacked on top of each other and if you play them all at the same time, you end up with a chord.</p>
