        <div class="image"><img src="../../images/guitar_and_keyboard/guitar-tab-strumming.jpg" alt="" class="img-style"></div>

        <p>While it may look confusing, it is simply asking you to strum an E minor chord eight times. (Note: We will look at chords in a later lesson)</p>

        <h3 class="border">Numbers Written Left to Right</h3>
        <p>When numbers are written side by side (on the same string or different strings), the Guitar TAB is telling you to play the notes one after the other from left to right.</p>
        <br><p>We read Guitar TAB just like you’re reading this sentence – from left to right one word at a time.</p>
        <br><p>In the below Guitar TAB, you start by playing the 1st fret note, then play the 2nd fret note, then the 3rd fret note, and finally the 4th fret note because they are all written one after the other.</p>
