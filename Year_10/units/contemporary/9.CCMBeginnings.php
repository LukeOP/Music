        <h3 class="border">The Great Awakening of Christian Music</h3>
        <p><strong> Plainchant</strong>, a form of monophonic, unaccompanied, sacred song became one of the pilars of the Roman Catholic Church during the 9th and 10th centuries.
            Gregorian Chants began to be sung by monks in monasteries using latin or greek text, and would sing excerpts from the Old Testament Psalms.</p>
        <p>This music could also be written out, but it looks a bit different to our modern music notation and is missing a lot of the detail we rely on now.
