    <h2>The Background of Contemporary Music</h2>
    <div class="text-content">
        <p>The background of contemporary music dates back to the end of the <a class="tooltip">Modern Music Era.<span class="tooltiptext">(1890-1975)</span></a>
            This is the musical period that preceded the contemporary era.
            It is safe to say the modern era largely influenced what would become contemporary music.
            This is because is also explored the use of <a class="tooltip">unconventional<span class="tooltiptext">different to what is normally done</span></a> music structures at that time.</p>
        <br>
        <p>In the same vein, artists that composed or performed contemporary music can be said to be <a class="tooltip">avant-gardist.<span class="tooltiptext">Someone that uses new or experimental ideas</span></a>
            This is considering how they outrightly broke the musical rule of that period.
            Sometimes, this involved the merging of 2 or more musical styles, playing unusual combinations on the musical instrument.</p>
        <br>
        <p>In the early stages, sub-categories such as <strong>minimalism</strong> and <strong>post-modernism</strong> were explored by the likes of <a class="text-link" href="https://en.wikipedia.org/wiki/Steve_Reich">Steve Reich</a>, <a class="text-link" href="https://en.wikipedia.org/wiki/Philip_Glass"> Phillip Glass</a> and <a href="https://en.wikipedia.org/wiki/John_Adams_(composer)" class="text-link">John Adams.</a></p>
