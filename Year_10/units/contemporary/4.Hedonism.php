        <h3 class="border">What is Hedonism?</h3>
        <p>Hedonism is a school of thought that argues that pleasure is the only <a class="tooltip">intrinsic <span class="tooltiptext">Something natural or true</span></a> good. In very simple terms, a hedonist strives to maximize net pleasure.</p>

        <h3>Historical Example:</h3>
        <p>In the original Old Babylonian version of the Epic of Gilgamesh, which was written briefly after the invention of writing, <a class="tooltip">Siduri <span class="tooltiptext">A character in the <i>Epic of Gilgamesh</i></span></a> gave the following advice...
        <div class="quote">
            <h3><img src="/images/quote.svg" alt="" class="icon">Siduri</h3>
            <p>"Fill your belly. Day and night make merry. Let days be full of joy. Dance and make music day and night [...] These things alone are the concern of men"</p>
        </div>
        <p>This may represent the first recorded advocacy of a hedonistic philosophy.</p>
        <br><br>
        <h3>Modern Example:</h3>
        <p>Hedonism is the belief that people should do whatever makes them happy and nothing else -
            a good example would be someone who doesn't like doing homework, so they go on the internet and try to get somebody else to write their paragraph while they go play video games and chat with their friends.​</p>

        <h3 class="border">Hedonism In Music</h3>
        <p>It is a well known fact that musicians and composers have the ability to express ideas in their music. To do this we can tell stories, give examples, or imply meaning through the choice of words or underlying themes.
            Sometimes the message is very clear, other times it lies just beneath the surface.</p>

