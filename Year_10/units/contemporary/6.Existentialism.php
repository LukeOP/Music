        <h3 class="border">What is Existentialism?</h3>
        <p>Each individual—not society or religion—is solely responsible for giving meaning to life and living it passionately and sincerely ("authentically").</p>
        <p><i>- Søren Kierkegaard (Philosopher)</i></p>

        <h3>Historical Example:</h3>
        <p>In the 19th century Søren Kierkegaard and Friedrich Nietzsche were two of the first philosophers considered important to the existentialist movement.
            They focused on subjective human experience rather than the objective truths of mathematics and science,
            which they believed were too detached or observational to truly get at the human experience.
            They were interested in people's quiet struggle with the apparent meaninglessness of life and the use of diversion to escape from boredom. </p>
        <br>
        <p>Fyodor Dostoyevsky, a Russian author wrote a book called “Notes from Underground”.
            It portrays a man unable to fit into society and unhappy with the identities he creates for himself.</p>
        <br>
        <p>Later, a Spanish philosopher wrote a book in which he stated “that human existence must always be defined as the individual person combined with the concrete circumstances of his life: (I am myself and my circumstances)”.</p>
        <br>


        <h3>Modern Example:</h3>
        <p>Existentialists insist on the inherent meaninglessness of the world.
            They often stress the importance of Angst as signifying the absolute lack of any objective ground for action.
            In existentialism, the individual's starting point is characterized by a sense of disorientation and confusion in the face of an apparently meaningless or absurd world.
            It is about “finding yourself” or “making a name for yourself”.</p>

        <h3 class="border">Existentialism In Music</h3>
        <p>It is a well known fact that musicians and composers have the ability to express ideas in their music.
            To do this we can tell stories, give examples, or imply meaning through the choice of words or underlying themes.
            Sometimes the message is very clear, other times it lies just beneath the surface.</p>
        <br>
        <p>How many times have we heard singers question their purpose in life, or even the meaning of life, in their music?</p>

