        <h3 class="border">What is Materialism?</h3>
        <p>Materialism is the preoccupation with or <strong> emphasis on material objects</strong>, comforts, and considerations, with disinterest in or rejection of spiritual, intellectual or cultural values.</p>

        <h3>Historical Example:</h3>
        <p>The idea of a materialistic society emerged in the late 17th century and intensified throughout the 18th century.
            Change was propelled by the growing middle-class who embraced new ideas about luxury consumption and the growing importance of fashion for purchasing rather than necessity.
            This revolution encompassed the growth in construction of vast country estates specifically designed to cater for comfort and the increased availability of luxury goods aimed at a growing market.</p>
        <br>

        <h3>Modern Example:</h3>
        <p>Materialism is the excessive desire to acquire and consume material goods.
            It is closely related to the perception that happiness can be increased through buying, spending and accumulating material wealth.
            We often hear people say “If only I had the newest iPhone I’d be happy”, or “I’m going to work through the holidays so I can have enough money to buy those shoes”.</p>

        <h3 class="border">Materialism In Music</h3>
        <p>It is a well known fact that musicians and composers have the ability to express ideas in their music.
            To do this we can tell stories, give examples, or imply meaning through the choice of words or underlying themes.
            Sometimes the message is very clear, other times it lies just beneath the surface.
            Popular musicians portray materialism through video all the time.
            Flash cars and fancy houses all generate an image of money.</p>

