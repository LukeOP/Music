            <p><br><strong>The Birth of Modern Worship</strong>
                <br><br>
                Whatever its localised expressions, the Jesus People Movement eventually permeated Christian culture in Britain through the music it brought with it.
                <br><br>
                Pete Ward, professor of ecclesiology and ethnography at Durham University, claims that the primary influence of the Jesus People Movement in the UK was a change of style:
                “The effect of the Jesus Movement was to breathe new life into the Church based on the appropriation of aspects of youth culture.”
                Such culture involved widespread changes in worship style. Out with the hymn-sandwich and in with the (previously sinful) guitar.
                This was largely the product of the movement’s informal style and the contemporary Christian music it generated.
                <br><br>
                Recently, the original editor of Buzz, Peter Meadows, spoke encouragingly about a three-fold impact the Californian movement had on Britain:
                Firstly, it shaped the lives of influential British Christians who went to the States to “look, see” (bringing back musicians like Larry Norman).
                Secondly, its Pentecostal roots, Californian laid-backness, and lack of religiosity poured more fuel on the charismatic renewal.
                And thirdly, Meadows says, “It lifted the morale and enthusiasm of the younger generation in the UK churches who, at last,
                had vivid examples of God at work in the world beyond the Church and in their own culture”.
                <br><br>
                The young radicals who found faith in the Jesus People Movement are now old enough to be entitled to free bus passes (and probably sport a lot less hair, too).
                And the movement itself only really burned brightly for as long as the hippy culture was with us.
                Yet its influence, like the youth counterculture it was part of, has shaped much of the evangelical Church today.
