        <h3 class="border">Christian Contemporary Music in the 1970s</h3>

        <h4>Quick Facts:</h4>
        <ul>
            <li>Christian Music in the 1970's <strong>Jesus Movement</strong> was very different to traditional Christian Music.</li>
            <li>It had hard hitting lyrics that spoke of people's problems and how God was fully in control of everything.</li>
            <li>New instruments sch as electric guitars, bass and drum kits were emerging.</li>
            <li>New artists like <strong>'Nancy Honeytree'</strong>, <strong>Larry Norman'</strong> and <strong>'the 2nd Chapter of Acts'</strong> became famous.</li>
        </ul>
        <br>
        <p>The 'Jesus Movement' of the 1970's changed the way people viewed Christian Music and invited many different people in using it's wide-spread genres.</p>

